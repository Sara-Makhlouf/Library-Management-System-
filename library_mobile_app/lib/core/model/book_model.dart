class BookModel {
  final String id;
  final String title;
  final String author;
  final String description;
  final String category;
  final String file;
  final double price;
  final int pages;
  BookModel({
    required this.id,
    required this.title,
    required this.author,
    required this.description,
    required this.category,
    required this.file,
    required this.price,
    required this.pages,
  });
}
